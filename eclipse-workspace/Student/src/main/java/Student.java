
public class Student {
	String name;
	String socre;
	
	public Student(String name, String socre) {
		super();
		this.name = name;
		this.socre = socre;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSocre() {
		return socre;
	}
	public void setSocre(String socre) {
		this.socre = socre;
	}
	
}
